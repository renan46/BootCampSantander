package GettersAndSetters;


public class Aluno{
    private String nome;
    private int idade;
    public String getNome() {
        return nome;
    }
    public void setNome(String newNome) {
        this.nome = newNome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int newidade) {
        this.idade = newidade;
    }

}

